// Q-1) Write a function to sort an array of numbers.
// Input: [8,5,6,3]
// Output: [3,5,6,8]

// input is given in the question
let input = [8, 5, 6, 3];
console.log("The input array is", input);

// "sort()" function will sort the array and save the sorted array value in "newInp" variable
let newInp = input.sort();

// newInp will be printed in the console
console.log("\nThe Output:", newInp);
